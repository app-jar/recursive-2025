% Проверка, является ли список фрагментом (подсписком) другого списка

% sublist(Sub, List) — истинно, если список Sub является фрагментом списка List
% Первый вариант: Sub совпадает с началом List
sublist(Sub, List) :-
    prefix(Sub, List).

% Второй вариант: иначе отбрасываем первый элемент списка List
% и рекурсивно проверяем оставшийся хвост Tail
sublist(Sub, [_|Tail]) :-
    sublist(Sub, Tail).

% prefix(P, L) — проверяет, является ли список P префиксом списка L

% Пустой список является префиксом любого списка
prefix([], _).

% Если первые элементы обоих списков совпадают,
% то продолжаем проверку оставшихся частей списков
prefix([H1|T1], [H2|T2]) :-
    H1 = H2,          % сравнение головных элементов
    prefix(T1, T2).   % рекурсивная проверка хвостов

