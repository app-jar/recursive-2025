% Задача 1 Вариант 9
% Получить список, содержащий все элементы исходного списка, но по одному.
% Основной предикат: получает список уникальных элементов
unique([], []).

unique([H|T], [H|Result]) :-
    % Удаляем все дубликаты H из оставшейся части списка
    remove_all(H, T, NewT),
    % Рекурсивно обрабатываем очищенный список
    unique(NewT, Result).

% Вспомогательный предикат: удаляет все вхождения элемента
remove_all(_, [], []).

% Если текущий элемент равен удаляемому - пропускаем его
remove_all(X, [X|T], Result) :-
    remove_all(X, T, Result).

% Если элемент отличается - сохраняем в результате
remove_all(X, [Y|T], [Y|Result]) :-
    X \= Y,
    remove_all(X, T, Result). 