% Номер 1 Задача 11
% Проверить что список является отсортированным

% Проверка отсортированности списка с пользовательским предикатом сравнения
is_sorted(Pred, List) :-
    callable(Pred),          % Проверяем, что Pred - вызываемый предикат
    is_list(List),           % Проверяем, что List - список
    is_sorted_impl(Pred, List), !.  % Проверяем отсортированность
is_sorted(_, _) :-
    fail.

% Базовые случаи: пустой список и список из одного элемента всегда отсортированы
is_sorted_impl(_, []) :- !.
is_sorted_impl(_, [_]) :- !.

% Рекурсивный случай: проверяем первые два элемента и продолжаем с хвостом
is_sorted_impl(Pred, [A,B|T]) :-
    call(Pred, A, B), !,           % Вызываем предикат сравнения для A и B
    is_sorted_impl(Pred, [B|T]).   % Рекурсия с хвостом списка

is_sorted_impl(_, _) :-
    fail.

% Специальный случай: проверка отсортированности по возрастанию (<)
is_sorted(List) :-
    is_sorted(<, List).
