% 8. Проверить, что все элементы списка различны.
% Базовый случай: пустой список всегда уникален.
all_unique([]).

% Рекурсивный случай: голова списка не должна быть в хвосте, и хвост тоже уникален.
all_unique([H|T]) :-
    \+ member(H, T),
    all_unique(T).

% Реализация member: проверяет, является ли элемент X членом списка.
% Базовый случай: X является головой списка.
member(X, [X|_]).

% Рекурсивный случай: X находится в хвосте списка.
member(X, [_|T]) :- member(X, T).
