% Номер 1. Задача №19.
% Сгенерировать список, содержащий первые N чисел Фибоначчи.

% Главный предикат — принимает N и возвращает список из первых N чисел Фибоначчи.
fibonacci_list(N, List) :-
    fibonacci_list(N, [0, 1], List).  % Начальные элементы последовательности.

% Базовый случай: если нужно 0 элементов — пустой список.
fibonacci_list(0, _, []).

% Базовый случай: если нужно 1 элемент — [0].
fibonacci_list(1, [A|_], [A]).

% Рекурсивный случай: если нужно больше 1 элемента.
fibonacci_list(N, [A,B|_], [A|Rest]) :-
    N > 1,
    N1 is N - 1,
    C is A + B,                       % Следующее число Фибоначчи.
    fibonacci_list(N1, [B,C], Rest).  % Рекурсия сдвигает последовательность.
