% Базовые случаи
levenshtein([], [], 0).                 % Две пустые строки
levenshtein([], B, D) :-                % Первая строка пуста
    length(B, D).
levenshtein(A, [], D) :-                % Вторая строка пуста
    length(A, D).

% Рекурсивный случай
levenshtein([A|As], [B|Bs], D) :-
    levenshtein(As, Bs, D1),            % Рекурсия для хвостов
    levenshtein(As, [B|Bs], D2),        % Удаление из A
    levenshtein([A|As], Bs, D3),        % Вставка в A
    min_list([D1, D2, D3], Min),        % Находим минимальное
    (A = B -> D = Min ; D is Min + 1).  % Учитываем совпадение
